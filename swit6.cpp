#include<bits/stdc++.h>
using namespace std;
int main()
{
	char ch = 'a';
	cout<<endl;
	
	switch(ch)
	{
		case 'a': cout<<"Dell"<<endl;
		break;
		
		case 'b': cout<<"HP"<<endl;
		break;
		
		case 'c': cout<<"ASUS"<<endl;
		break;
		
		case 'd': cout<<"Omen"<<endl;
		break;
		
		default :cout<<"All are the standards laptops"<<endl;
		break;
	}
	return  0;
}
