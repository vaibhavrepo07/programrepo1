#include<bits/stdc++.h>
using namespace std;
int main()
{
	char ch= '1';
	cout<<endl;
	
	switch(ch)
	{
		case 1:cout<<"First"<<endl;
		break;
		case 2: cout<<"Second"<<endl;
		break;
		case '1': cout<<"Third"<<endl;
		break;
		
		default : cout<<"It is default case"<<endl;
	}
	cout<<endl;
	
	return 0;
}

