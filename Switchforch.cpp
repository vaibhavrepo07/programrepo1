#include<bits/stdc++.h>
using namespace std;
int main()
{
	char ch='2';
	
	cout<<endl;
    switch(ch)
                 {
                 	case 1: cout<<"First";
                 	break;
                 	
                 	case '2':cout<<"Second";
                 	break;
                 	
                 	case 3 :cout<<"Third";
                 	break;
                 	
                 	default:
                 		cout<<"End of Switch";
				 }
				 
				 return 0;
}
