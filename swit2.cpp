#include<bits/stdc++.h>
using namespace std;
int main()
{
	int num = 3;
	cout<<endl;
	
	switch(num)
	{
		case 1: cout<<"Hello"<<endl;
		break;
		case 2: cout<<"Brother"<<endl;
		break;
		case 3: cout<<"How are you"<<endl;
		break;
		case 4: cout<<"Good"<<endl;
		break;
		
		default :cout<<"It is default case"<<endl;
	}
	
	return 0;
}
